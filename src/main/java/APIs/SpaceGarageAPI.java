@ApplicationPath("api")
public class SpaceGarageAPI extends Application {

    @Override
    public Set<Class<?>> getClasses() {
        Set<Class<?>> resources = new HashSet<>();

        // Hier für jede Webservice-Klasse eine Zeile hinzufügen
        resources.add(SpaceGarageResource.class);

        return resources;
    }

}
